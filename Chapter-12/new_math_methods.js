//Truncate method
const a = Math.trunc(123.5564656)
const b = Math.trunc(-143.67576)
const c = Math.trunc("12.43535")
console.log(a + "\n" + b + "\n" + c + "\n");

//Sign method
const d = Math.sign(-1223);
const e = Math.sign(1244);
const f = Math.sign(0);
console.log(d + "\n" + e + "\n" + f + "\n");

//Cube Root Method
const w = Math.cbrt(8)
const x = Math.cbrt(27)
const y = Math.cbrt(64)
const z = Math.cbrt(125)
console.log(w + "\n" + x + "\n" + y + "\n" + z + "\n");

//Log with Base 2
const p = Math.log2(2)
const q = Math.log2(8)
const r = Math.log2(4)
console.log(p + "\n" + q + "\n" + r + "\n");

//Log with Base 10
const m = Math.log10(1)
const n = Math.log10(10)
const o = Math.log10(100)
console.log(m + "\n" + n + "\n" + o + "\n");